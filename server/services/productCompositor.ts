// EFFECTS
// ============================================================

/**
 * Create a soft drop shadow from the product silhouette.
 * Produces a proper RGBA buffer so Sharp composites it correctly without a black box.
 */
async function createDropShadow(
  productBuffer: Buffer,
  width: number,
  height: number
): Promise<Buffer> {
  // Extract the alpha channel as a greyscale mask
  const alphaMask = await sharp(productBuffer)
    .resize(width, height)
    .ensureAlpha()
    .extractChannel(3)
    .blur(14)
    .toBuffer();

  // Build a black RGBA image the same size, using the blurred alpha mask as opacity
  // This produces a proper RGBA shadow buffer that Sharp can composite correctly
  const blackLayer = await sharp({
    create: {
      width,
      height,
      channels: 4,
      background: { r: 0, g: 0, b: 0, alpha: 0 },
    },
  })
    .png()
    .toBuffer();

  // Composite the alpha mask onto the black layer to produce a dark semi-transparent shadow
  return sharp(blackLayer)
    .composite([{ input: alphaMask, blend: 'dest-in' }])
    .modulate({ brightness: 0.4 })
    .png()
    .toBuffer();
}

/**
 * Create a glow effect around the product silhouette.
 */
async function createGlowEffect(
  productBuffer: Buffer,
  width: number,
  height: number,
  color: string
): Promise<Buffer> {
  // Parse hex colour
  const r = parseInt(color.slice(1, 3), 16);
  const g = parseInt(color.slice(3, 5), 16);
  const b = parseInt(color.slice(5, 7), 16);

  // Create a slightly larger canvas for the glow
  const glowWidth = width + 20;
  const glowHeight = height + 20;

  // Extract alpha, tint with glow colour, blur heavily
  const alphaGlow = await sharp(productBuffer)
    .resize(width, height)
    .ensureAlpha()
    .extractChannel(3)
    .toColourspace("b-w")
    .tint({ r, g, b })
    .blur(20)
    .extend({
      top: 10, bottom: 10, left: 10, right: 10,
      background: { r: 0, g: 0, b: 0, alpha: 0 },
    })
    .toBuffer();

  return alphaGlow;
}

