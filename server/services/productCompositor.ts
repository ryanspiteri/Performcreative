/**
 * Product Compositor — Two-Pass Image Compositing
 * 
 * Solves the AI label accuracy problem by:
 * 1. Generating background/scene with Gemini (no product in the image)
 * 2. Compositing the real product render PNG on top using sharp
 * 
 * This guarantees pixel-perfect product labels because the actual
 * uploaded product render is used, not an AI approximation.
 */

import sharp from "sharp";
import axios from "axios";
import { storagePut } from "../storage";

// ============================================================
// TYPES
// ============================================================

export interface CompositeOptions {
  /** The AI-generated background image URL (no product in it) */
  backgroundUrl: string;
  /** The real product render PNG URL (with transparent background) */
  productRenderUrl: string;
  /** Where to place the product: "center", "left", "right", "bottom-center" */
  productPosition?: ProductPosition;
  /** Product size as percentage of canvas width (0.2 = 20%, 0.5 = 50%) */
  productScale?: number;
  /** Vertical offset from the calculated position (negative = up, positive = down) */
  verticalOffset?: number;
  /** Optional drop shadow behind the product */
  addShadow?: boolean;
  /** Optional glow effect around the product */
  addGlow?: boolean;
  /** Glow colour in hex (default: based on product) */
  glowColor?: string;
}

export type ProductPosition = "center" | "left" | "right" | "bottom-center" | "bottom-left" | "bottom-right";

export interface CompositeResult {
  imageUrl: string;
  s3Key: string;
  width: number;
  height: number;
}

// ============================================================
// FETCH HELPER
// ============================================================

async function fetchImageBuffer(url: string): Promise<Buffer> {
  const response = await axios.get(url, {
    responseType: "arraybuffer",
    timeout: 30000,
    headers: { "User-Agent": "Mozilla/5.0" },
  });
  return Buffer.from(response.data);
}

// ============================================================
// MAIN COMPOSITOR
// ============================================================

/**
 * Composite a real product render onto an AI-generated background.
 * 
 * The product render should be a PNG with transparent background.
 * The background should be a full scene generated by Gemini WITHOUT
 * any product in it.
 */
export async function compositeProductOnBackground(
  options: CompositeOptions
): Promise<CompositeResult> {
  const {
    backgroundUrl,
    productRenderUrl,
    productPosition = "center",
    productScale = 0.45,
    verticalOffset = 0,
    addShadow = false,
    addGlow = false,
    glowColor = "#00ff88",
  } = options;

  console.log("[Compositor] Starting two-pass compositing...");
  console.log(`[Compositor] Background: ${backgroundUrl}`);
  console.log(`[Compositor] Product render: ${productRenderUrl}`);
  console.log(`[Compositor] Position: ${productPosition}, Scale: ${productScale}`);

  // Step 1: Fetch both images
  const [bgBuffer, productBuffer] = await Promise.all([
    fetchImageBuffer(backgroundUrl),
    fetchImageBuffer(productRenderUrl),
  ]);

  // Step 2: Get background dimensions
  const bgMeta = await sharp(bgBuffer).metadata();
  const bgWidth = bgMeta.width || 1024;
  const bgHeight = bgMeta.height || 1024;
  console.log(`[Compositor] Background size: ${bgWidth}x${bgHeight}`);

  // Step 3: Resize product render to target scale
  const targetProductWidth = Math.round(bgWidth * productScale);
  const resizedProduct = await sharp(productBuffer)
    .resize({ width: targetProductWidth, fit: "inside" })
    .ensureAlpha()
    .toBuffer();

  const productMeta = await sharp(resizedProduct).metadata();
  const productWidth = productMeta.width || targetProductWidth;
  const productHeight = productMeta.height || targetProductWidth;
  console.log(`[Compositor] Product resized to: ${productWidth}x${productHeight}`);

  // Step 4: Calculate position
  const { left, top } = calculatePosition(
    bgWidth, bgHeight,
    productWidth, productHeight,
    productPosition,
    verticalOffset
  );
  console.log(`[Compositor] Product position: left=${left}, top=${top}`);

  // Step 5: Build composite layers
  const layers: sharp.OverlayOptions[] = [];

  // Optional: Add drop shadow behind the product
  if (addShadow) {
    const shadowBuffer = await createDropShadow(resizedProduct, productWidth, productHeight);
    layers.push({
      input: shadowBuffer,
      left: Math.max(0, left + 8),
      top: Math.max(0, top + 8),
      blend: "over" as const,
    });
  }

  // Optional: Add glow effect
  if (addGlow) {
    const glowBuffer = await createGlowEffect(resizedProduct, productWidth, productHeight, glowColor);
    layers.push({
      input: glowBuffer,
      left: Math.max(0, left - 10),
      top: Math.max(0, top - 10),
      blend: "over" as const,
    });
  }

  // Add the actual product render
  layers.push({
    input: resizedProduct,
    left: Math.max(0, left),
    top: Math.max(0, top),
    blend: "over" as const,
  });

  // Step 6: Composite all layers onto the background
  const composited = await sharp(bgBuffer)
    .composite(layers)
    .png({ quality: 95 })
    .toBuffer();

  console.log(`[Compositor] Compositing complete. Output size: ${composited.length} bytes`);

  // Step 7: Upload to S3
  const timestamp = Date.now();
  const randomSuffix = Math.random().toString(36).substring(2, 8);
  const s3Key = `composited-ads/${timestamp}-${randomSuffix}.png`;
  const { url: imageUrl } = await storagePut(s3Key, composited, "image/png");

  console.log(`[Compositor] Uploaded to S3: ${imageUrl}`);

  return {
    imageUrl,
    s3Key,
    width: bgWidth,
    height: bgHeight,
  };
}

// ============================================================
// POSITION CALCULATOR
// ============================================================

function calculatePosition(
  bgWidth: number, bgHeight: number,
  productWidth: number, productHeight: number,
  position: ProductPosition,
  verticalOffset: number
): { left: number; top: number } {
  let left: number;
  let top: number;

  switch (position) {
    case "center":
      left = Math.round((bgWidth - productWidth) / 2);
      top = Math.round((bgHeight - productHeight) / 2);
      break;
    case "left":
      left = Math.round(bgWidth * 0.08);
      top = Math.round((bgHeight - productHeight) / 2);
      break;
    case "right":
      left = Math.round(bgWidth * 0.92 - productWidth);
      top = Math.round((bgHeight - productHeight) / 2);
      break;
    case "bottom-center":
      left = Math.round((bgWidth - productWidth) / 2);
      top = Math.round(bgHeight * 0.95 - productHeight);
      break;
    case "bottom-left":
      left = Math.round(bgWidth * 0.08);
      top = Math.round(bgHeight * 0.95 - productHeight);
      break;
    case "bottom-right":
      left = Math.round(bgWidth * 0.92 - productWidth);
      top = Math.round(bgHeight * 0.95 - productHeight);
      break;
    default:
      left = Math.round((bgWidth - productWidth) / 2);
      top = Math.round((bgHeight - productHeight) / 2);
  }

  // Apply vertical offset
  top += verticalOffset;

  // Clamp to canvas bounds
  left = Math.max(0, Math.min(left, bgWidth - productWidth));
  top = Math.max(0, Math.min(top, bgHeight - productHeight));

  return { left, top };
}

