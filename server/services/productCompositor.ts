/**
 * Get a description of where to leave space for the product.
 */
function getSpaceDescription(position: ProductPosition): string {
  switch (position) {
    case "center":
      return "Leave the centre of the image clear and unobstructed for the product";
    case "left":
      return "Leave the left side of the image clear and unobstructed for the product";
    case "right":
      return "Leave the right side of the image clear and unobstructed for the product";
    case "bottom-center":
      return "Leave the bottom-centre area clear and unobstructed for the product";
    case "bottom-left":
      return "Leave the bottom-left area clear and unobstructed for the product";
    case "bottom-right":
      return "Leave the bottom-right area clear and unobstructed for the product";
    default:
      return "Leave the centre of the image clear and unobstructed for the product";
  }
}
